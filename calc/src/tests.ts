import calc from './calc.js';


console.log("\nПримеры ошибок:");

// 1. Несбалансированные скобки
calc("*( + 2 3 4"); // Error: Несбалансированные скобки

// 2. Недостаточно операндов
calc("* + 2 3"); // Error: Недостаточно операндов для оператора

// 3. Деление на ноль
calc("/ 10 ( - 5 5 )"); // Error: Деление на ноль

// 4. Неизвестный оператор
calc("& 5 3"); // Error: Неизвестный оператор

// 5. Неверный числовой токен
calc("+ 5 abc"); // Error: Неверный числовой токен

// 6. Пустое выражение
calc(""); // Error: Пустое выражение

// 7. Лишние операнды
calc("+ 2 3 4"); // Error: Неверное выражение

// 8. Скобки без содержимого
calc("( )"); // Error: Пустое выражение

console.log("Сложные примеры:");
// 1. Комбинированные вложенные операции
calc("* ( + ( - 20 ( / 10 2 ) ) 3 ) ( - ( * 4 3 ) ( + 1 1 ) )"); // ((20-5)+3) * (12-2) = 18 * 10 = 180

// 2. Много уровней скобок
calc("( + ( ( * 2 ( - 8 1 ) ) ) ( ( / ( * 3 4 ) 2 ) ) )"); // (2*7) + (12/2) = 14 + 6 = 20

// 3. Сложная арифметика
calc("/ ( - ( + ( * 3 5 ) 10 ) 5 ) ( + ( - 8 3 ) 1 )"); // ((15+10)-5) / (5+1) = 20 / 6 ≈ 3.333

// 4. Глубокая вложенность
calc("+ ( * ( - ( / 30 2 ) 5 ) 2 ) ( / ( * 4 3 ) ( + 1 1 ) )"); // ((15-5)*2) + (12/2) = 20 + 6 = 26



// 5. Комплексное выражение с многими операциями
calc("- ( + ( * 2 ( - 10 2 ) ) ( / 16 4 ) ) ( * 3 ( + 1 1 ) )"); // (2*8 + 4) - (3*2) = (16+4) - 6 = 20 - 6 = 14

// 6. Максимальная вложенность
calc("* ( + ( - ( / ( * 2 3 ) 2 ) 1 ) 4 ) ( - ( + 10 2 ) ( * 2 1 ) )"); // (((6/2)-1)+4) * (12-2) = (3-1+4) * 10 = 6 * 10 = 60

console.log("\nКорректные примеры для сравнения:");
// Корректные аналоги
calc("-1");  // -1 именно как значение, операция такая в унарном виде по сути не допустима
calc("+ 2 ( * 3 4 )"); // 14
calc("*( + 2 3 ) 4"); // 20
calc("* 2 3"); // 6
calc("/ 10 2"); // 5
calc("+ 5 3"); // 8
calc("+ 2 3"); // 5
calc("( + 2 3 )"); // 5